export interface IDocument {
  _id: string;
  id: string;
  createdAt: string;
  updatedAt: string;
  deleted: boolean;
}
export interface IAssetBucket {
  name: string;
  url: string;
  path: string;
}

export interface IUser extends IDocument {
  _id: string;
  name: string;
  email: string;
  image: string;
  role: string;
  country: string;
  timezone: string;
  language: string;
  currency: string;
  attributes?: Record<string, string | number | boolean>;
}

export interface IAsset extends IDocument {
  _id: string;
  createdAt: string;
  updatedAt: string;
  deleted: boolean;
  name: string; // original name
  uniqueName: string; // unique name generated by multer
  mimeType: string; // mime type
  size: number; // size in bytes
  encoding: string; // encoding
  bucket: {
    name: string;
    url: string;
    path: string;
  };
  metadata: {
    [key: string]: string | number | boolean | object | null;
  };
  // image only
  image?: {
    width: number;
    height: number;
  };
  tags: string[];
  public: boolean;
  creator: string | IUser;
}
